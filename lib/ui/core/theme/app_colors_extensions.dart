import 'package:flutter/material.dart';

class AppColorsExtensions extends ThemeExtension<AppColorsExtensions> {
  AppColorsExtensions();

  @override
  ThemeExtension<AppColorsExtensions> copyWith({Color? success}) {
    return AppColorsExtensions();
  }

  @override
  ThemeExtension<AppColorsExtensions> lerp(covariant ThemeExtension<AppColorsExtensions>? other, double t) {
    return AppColorsExtensions();
  }
}
